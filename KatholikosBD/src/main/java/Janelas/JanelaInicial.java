    public static JanelaInicial getJanelaIncioal () {
        if (janelaInicial == null) {
            janelaInicial = new JanelaInicial ();
            janelaInicial.situacaoConexao();
        }
        return janelaInicial;
    }

    public final boolean situacaoConexao () {
        if (janelaInicial.bancoDados.getConexao() == null){
            janelaInicial.btConectaBD.setBackground(Color.red);
            janelaInicial.btConectaBD.setText("Desconectado");
            janelaInicial.btConectaBD.setForeground(Color.black);
            return false;
        } else {
            janelaInicial.btConectaBD.setBackground(Color.GREEN);
            janelaInicial.btConectaBD.setText("Conectado");
            janelaInicial.btConectaBD.setForeground(Color.white);
            return true;
        }
    }
    
