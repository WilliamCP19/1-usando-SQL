    private void lbsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lbsalvarActionPerformed
        switch (conEditora.getConBD().getValidar()) {
        case 1:
            if (preencherEditora()) {
                if (conEditora.inserir()) {
                    conEditora.getConBD().mensagens(conEditora.getConBD().getValidar());
                }
            }
            break;
        case 2:
            if (preencherEditora()) {
                if (conEditora.editar(id)) {
                   conEditora.getConBD().mensagens(conEditora.getConBD().getValidar()); 
                }
            }
            break;
        case 3:
            lbsalvar.enableInputMethods(false);
            break;
    }
        this.dispose(); zeraCampos();
    }//GEN-LAST:event_lbsalvarActionPerformed

