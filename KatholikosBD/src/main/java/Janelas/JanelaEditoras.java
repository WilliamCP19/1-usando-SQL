    public final boolean preencherEditora () {
        try {
            conEditora.getEditora().setNome(cxNome.getText());
            conEditora.getEditora().setFundador(cxFundador.getText());
            conEditora.getEditora().setPresidente(cxPresidente.getText());
            conEditora.getEditora().setSede(cxSede.getText());
            conEditora.getEditora().setAnoFund(Integer.parseInt(cxAnoFund.getText()));
            conEditora.getEditora().setSituacao(rbAtiva.isSelected());
            conEditora.getEditora().setWebsite(cxWebsite.getText());
            return true;
        } catch (NumberFormatException num){
            conEditora.getConBD().informativo("Ano de fundação deve ser número");
            return false;
        }
    }
    
    public void setCampos (int id) {
        try {
            conEditora.getConBD().setConjuntos(conEditora.getConBD().getConexao().createStatement(1005,1008).executeQuery("SELECT * FROM editora WHERE ed_id = "+id+";"));
            if (conEditora.getConBD().getConjuntos().first()) {
                cxNome.setText(conEditora.getConBD().getConjuntos().getString("ed_nome"));
                cxFundador.setText(conEditora.getConBD().getConjuntos().getString("ed_fundador"));
                cxPresidente.setText(conEditora.getConBD().getConjuntos().getString("ed_presidente"));
                cxSede.setText(conEditora.getConBD().getConjuntos().getString("ed_sede"));
                cxAnoFund.setText(conEditora.getConBD().getConjuntos().getString("ed_anoFund"));
                cxWebsite.setText(conEditora.getConBD().getConjuntos().getString("ed_website"));
                rbAtiva.setSelected(conEditora.getConBD().getConjuntos().getString("ed_situacao").equals("ATIVA"));
                rbDesativada.setSelected(!rbAtiva.isSelected());
                conEditora.getEditora().setId(conEditora.getConBD().getConjuntos().getInt("ed_id"));
                preencherEditora();
                conEditora.getConBD().getConjuntos().close();
            }
        } catch (SQLException ex) {
            conEditora.getConBD().errosSQL("Não foi possível alterar os campos com as informações do banco de dados", ex);
        }
    }
    
    public void zeraCampos () {
        cxNome.setText("");
        cxFundador.setText("");
        cxSede.setText("");
        cxPresidente.setText("");
        cxWebsite.setText("");
        cxAnoFund.setText("");
        rbAtiva.setSelected(false);
        rbDesativada.setSelected(rbAtiva.isSelected());
    }
    
